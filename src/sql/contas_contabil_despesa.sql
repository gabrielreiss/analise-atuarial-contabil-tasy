SELECT
    T1.DT_REFERENCIA competencia,
    T1.CD_CONTA_CONTABIL CD_CONTA_CONTABIL,
    T1.DS_CONTA_CONTABIL DS_CONTA_CONTABIL,
    T1.DS_GRUPO_CONTA DS_GRUPO_CONTA,
